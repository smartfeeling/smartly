package org.smartly.commons.image;

/**
 *
 */
public class ImageSize {

    private int _width;
    private int _height;

    public ImageSize() {
        _width = 0;
        _height = 0;
    }

    public int getWidth() {
        return _width;
    }

    public void setWidth(int width) {
        _width = width;
    }

    public int getHeight() {
        return _height;
    }

    public void setHeight(int height) {
        _height = height;
    }


}
